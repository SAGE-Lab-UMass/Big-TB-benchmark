# Mapping of drug names to their index in the phenotype matrix (fallback only)
# WARNING: This is a hardcoded mapping and may not reflect the actual order in the NPZ files. Double check the order in your data and update if necessary.
DRUG_INDEX = {
    "AMIKACIN": 0,
    "CAPREOMYCIN": 1,
    "ETHAMBUTOL": 2,
    "ETHIONAMIDE": 3,
    "ISONIAZID": 4,
    "KANAMYCIN": 5,
    "LEVOFLOXACIN": 6,
    "MOXIFLOXACIN": 7,
    "PYRAZINAMIDE": 8,
    "RIFAMPICIN": 9,
    "STREPTOMYCIN": 10,
}


def _infer_drug_index(label_file, drug):
    """
    Automatically infer the drug index from the phenotype file.

    Attempts to find the drug index by checking:
    1. If the NPZ file contains drug names metadata
    2. If the drug exists in DRUG_INDEX (fallback to hardcoded mapping)

    Args:
        label_file (str): Path to NPZ file containing phenotype data.
        drug (str): Drug name to find the index for.

    Returns:
        int: Column index of the drug in the phenotype matrix.

    Raises:
        KeyError: If drug index cannot be inferred from available data.
    """
    label_data = np.load(label_file, allow_pickle=True)
    
    # Try to get drug names from NPZ metadata
    if "drug_names" in label_data:
        drug_names = label_data["drug_names"]
        if isinstance(drug_names, np.ndarray):
            drug_names = drug_names.tolist()
        try:
            drug_index = drug_names.index(drug)
            print(f"  Drug index inferred from NPZ metadata: {drug_index}")
            return drug_index
        except ValueError:
            print(f"  Warning: Drug '{drug}' not found in NPZ metadata")
    
    # Fall back to hardcoded DRUG_INDEX mapping
    if drug in DRUG_INDEX:
        drug_index = DRUG_INDEX[drug]
        print(f"  Drug index inferred from hardcoded mapping: {drug_index}")
        return drug_index
    
    # If all else fails, raise an error
    raise KeyError(
        f"Could not infer drug index for '{drug}'. "
        f"Available methods: NPZ metadata, hardcoded DRUG_INDEX. "
        f"Consider adding '{drug}' to DRUG_INDEX or NPZ metadata."
    )


def build_label_map(label_file, drug, prefix="full"):
    """
    Build a label map for a specific drug from phenotype data.

    Creates a mapping from sample identifiers to binary phenotype labels
    (0 = resistant, 1 = sensitive), filtering out invalid labels (-1).
    Automatically infers the drug index from available metadata.

    Args:
        label_file (str): Path to NPZ file containing phenotype matrix.
                          Expected key: "phenotypes" with shape (num_samples, num_drugs).
                          Optional keys: "drug_names" or "drugs" for metadata.
        drug (str): Drug name to extract labels for.
        prefix (str): Prefix for sample identifiers (default: "full").
                      Output IDs will be formatted as "{prefix}_{index:06d}".

    Returns:
        tuple: (label_map, drug_index)
            - label_map (dict): Maps sample IDs to binary phenotype labels.
            - drug_index (int): Column index of the drug in the phenotype matrix.

    Raises:
        KeyError: If drug index cannot be inferred from available data sources.
    """
    print(f"Loading labels from: {label_file}")

    # Automatically infer drug index
    drug_index = _infer_drug_index(label_file, drug)

    # Load phenotype data
    label_data = np.load(label_file)
    phenotypes = label_data["phenotypes"]  # Shape: (num_samples, num_drugs)
    drug_labels = phenotypes[:, drug_index]

    print(f"Drug: {drug} (column index {drug_index})")
    print(f"Total samples (including missing labels): {len(drug_labels)}")

    # Filter out invalid labels (-1)
    valid_indices = np.where(drug_labels != -1)[0]
    valid_labels = drug_labels[valid_indices]
    print(f"Valid samples with labels: {len(valid_labels)}")

    # Build sample ID to label mapping
    label_map = {
        f"{prefix}_{i:06d}": float(valid_labels[j])
        for j, i in enumerate(valid_indices)
    }

    return label_map, drug_index


def _create_dataset(embedding_type, memmap_dir, genes, label_map, pca_components=10):
    """
    Create appropriate dataset object based on embedding type and gene configuration.

    Args:
        embedding_type (str): Type of embeddings ("token", "mean", or "pca").
        memmap_dir (str): Directory containing memmap embedding files.
        genes (list): List of gene names to include.
        label_map (dict): Sample ID to label mapping.
        pca_components (int): Number of PCA components (only used if embedding_type == "pca").

    Returns:
        Dataset: Appropriate dataset object for the given configuration.
    """
    if len(genes) == 1:
        # Single-gene dataset (e.g., ISONIAZID -> katG)
        gene = genes[0]
        meta_paths = sorted(glob.glob(f"{memmap_dir}/{gene}/*_meta.npz"))
        print(f"\nCreating single-gene dataset")
        print(f"Found {len(meta_paths)} meta files")

        if embedding_type == "token":
            return TokenMemmapMap(meta_paths, label_map)
        elif embedding_type == "pca":
            return PcaMemmapMap(meta_paths, label_map, k=pca_components)
        else:  # mean
            return MeanMemmapMap(meta_paths, label_map, embed_type=embedding_type)
    else:
        # Multi-gene dataset
        gene_memmap_dirs = [f"{memmap_dir}/{gene}/" for gene in genes]
        print(f"\nCreating multi-gene dataset")

        if embedding_type == "token":
            return MultiGeneConcatDataset(gene_memmap_dirs, label_map)
        elif embedding_type == "pca":
            return PcaMultiGeneConcatDataset(gene_memmap_dirs, label_map, k=pca_components)
        else:  # mean
            return MeanMultiGeneConcatDataset(gene_memmap_dirs, label_map, embed_type=embedding_type)


def _setup_class_weights(train_labels, device):
    """
    Calculate class weights for imbalanced datasets.

    Computes positive weight for BCEWithLogitsLoss to handle class imbalance
    by weighting the minority class more heavily.

    Args:
        train_labels (np.ndarray): Binary labels for training set.
        device (torch.device): Device to place tensor on.

    Returns:
        tuple: (pos_weight, num_resistant, num_sensitive)
    """
    num_resistant = np.sum(train_labels == 0)
    num_sensitive = np.sum(train_labels == 1)
    
    pos_weight = torch.tensor(
        num_sensitive / (num_resistant + 1e-8),
        dtype=torch.float32,
    ).to(device)
    
    return pos_weight, num_resistant, num_sensitive


def main(args):
    """
    Main training pipeline for single-drug resistance classification.

    Orchestrates the full training workflow:
    1. Setup GPU environment
    2. Load and prepare datasets
    3. Perform stratified train/test split
    4. Configure loss function with class weighting
    5. Execute 5-fold cross-validation training

    Args:
        args: Command-line arguments with training configuration.
    """
    # GPU setup
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    n_gpu = torch.cuda.device_count()
    print(f"GPUs available: {n_gpu}")

    print("=" * 70)
    print("DNABERT-S Single-Drug Resistance Classification Training")
    print("=" * 70)
    print(f"Drug: {args.drug}")
    print(f"Embedding type: {args.embed_type}")
    print(f"Model architecture: {args.model_name}")
    print(f"Output directory: {args.saved_model_path}")
    print("=" * 70)

    # -----------------------------------------------
    # Load phenotype labels and prepare dataset
    # -----------------------------------------------
    print("\nLoading and preparing dataset...")
    full_label_map, drug_index = build_label_map(
        args.phenotype_label_path,
        args.drug,
        prefix="full"
    )

    # Get genes for this drug
    target_genes = DRUG_TO_LOCI[args.drug]
    print(f"\nGenes associated with {args.drug}: {target_genes}")

    # Create dataset
    full_dataset = _create_dataset(
        args.embed_type,
        args.saved_embed_memmap_dir,
        target_genes,
        full_label_map,
        pca_components=args.pca_components,
    )

    # Get embedding dimensions from first sample
    sample_embedding, _ = full_dataset[0]
    embedding_dim = sample_embedding.shape[0]
    sequence_len = sample_embedding.shape[1]

    print(f"\nDataset Information:")
    print(f"  Total samples: {len(full_dataset)}")
    print(f"  Embedding dimension: {embedding_dim}")
    print(f"  Sequence length: {sequence_len}")
    print(f"  Sample shape: {sample_embedding.shape}")

    # -----------------------------------------------
    # Stratified train/test split
    # -----------------------------------------------
    print(f"\nPerforming stratified train/test split (test_size={args.test_split})...")

    all_labels = np.array(list(full_label_map.values()))
    
    train_indices, test_indices = train_test_split(
        np.arange(len(all_labels)),
        test_size=args.test_split,
        stratify=all_labels,
        random_state=args.random_seed,
    )

    train_dataset = Subset(full_dataset, train_indices)
    train_labels = all_labels[train_indices]
    test_labels = all_labels[test_indices]

    # Analyze class distribution
    train_resistant = np.sum(train_labels == 0)
    train_sensitive = np.sum(train_labels == 1)
    test_resistant = np.sum(test_labels == 0)
    test_sensitive = np.sum(test_labels == 1)

    print(f"\nData split:")
    print(f"  Training set: {len(train_dataset)} samples")
    print(f"    - Resistant (R=0): {train_resistant}")
    print(f"    - Sensitive (S=1): {train_sensitive}")
    print(f"  Test set: {len(test_indices)} samples")
    print(f"    - Resistant (R=0): {test_resistant}")
    print(f"    - Sensitive (S=1): {test_sensitive}")

    # -----------------------------------------------
    # Setup loss function with class weighting
    # -----------------------------------------------
    pos_weight, num_resistant, num_sensitive = _setup_class_weights(
        train_labels,
        device
    )
    
    criterion = torch.nn.BCEWithLogitsLoss(pos_weight=pos_weight)
    print(f"\nLoss function: BCEWithLogitsLoss (pos_weight={pos_weight.item():.3f})")

    # -----------------------------------------------
    # 5-fold cross-validation training
    # -----------------------------------------------
    print(f"\nStarting 5-fold cross-validation training...")
    print(f"  Epochs: {args.num_epochs}")
    print(f"  Learning rate: {args.learning_rate}")
    print(f"  Weight decay: {args.weight_decay}")
    print(f"  Train batch size: {args.train_batch_size}")
    print(f"  Val batch size: {args.val_batch_size}")

    cross_val_train_on_token_embeddings(
        dataset=train_dataset,
        drug=args.drug,
        num_sensitive=num_sensitive,
        num_resistant=num_resistant,
        criterion=criterion,
        learning_rate=args.learning_rate,
        weight_decay=args.weight_decay,
        output_path=args.output_path,
        saved_model_path=args.saved_model_path,
        model_name=args.model_name,
        model_dim=embedding_dim,
        model_seq_len=sequence_len,
        k_folds=5,
        epochs=args.num_epochs,
        train_batch_size=args.train_batch_size,
        val_batch_size=args.val_batch_size,
        freeze_bias_frac=0.25,
        random_seed=42,
        device=device,
    )

    print("\n" + "=" * 70)
    print("Training completed successfully!")
    print(f"Model checkpoints: {args.saved_model_path}")
    print(f"Results and metrics: {args.output_path}")
    print("=" * 70)


